print('####Eval Function to decide which type of data type:####')
x1=eval(input('Entry X1?'))
print('x1 =', x1 , 'Type:' , type(x1))
x2=eval(input('Entry X2?'))
print('x2 =', x2 , 'Type:' , type(x2))

